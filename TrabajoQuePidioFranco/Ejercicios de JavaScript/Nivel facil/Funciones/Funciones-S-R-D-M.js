function sumar (num1,num2) {
  return num1 + num2;
};

function restar (num1,num2) {
  return num1 - num2;
};

function dividir (num1,num2) {
  return num1 / num2;
};

function multiplicar (num1,num2) {
  return num1 * num2;
};

console.log (sumar(50,25));
console.log (restar(90,15));
console.log (dividir(150,2));
console.log (multiplicar(5,15));